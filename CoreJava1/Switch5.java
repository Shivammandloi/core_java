package com.rays.CoreJava1;

public class Switch5 {
	public static void main(String[] args) {
		int num = 11;

		switch (num) {
		case 34:
			System.out.println(" yes sir");
			break;
		case 46:
			System.out.println("no sir");
			break;
		case 67:
			System.out.println("yes sir1");
			break;
		case 11:
			System.out.println("no sir1");

		case 32:
			System.out.println("yes sir2");

		case 56:
			System.out.println("no sir2");

		case 93:
			System.out.println("yes");
		default:

			System.out.println("this is defalt block");

		}
	}

}
