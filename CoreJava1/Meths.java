package com.rays.CoreJava1;

public class Meths {
	public static void main(String[] args) {
		System.out.println("Mathematics functions");
		System.out.println("Max 11,51 ="+Math.max(11, 51));
		System.out.println("Min 2,20 ="+Math.min(2, 20));
		System.out.println("Absolute 3.7 ="+ Math.abs(3.7));
		System.out.println("Exp 10 ="+Math.exp(10));
		System.out.println("cube 2="+Math.pow(2, 3));
		System.out.println("Random Number = "+Math.random());
		System.out.println("Square Root ="+Math.sqrt(4));
		
	}

}
